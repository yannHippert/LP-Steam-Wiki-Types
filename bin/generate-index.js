const fs = require('fs');
const path = require('path');

const packageName = 'steam-wiki';
const typesFolder = 'source';

function getExportsLines(folderPath) {
    const items = fs.readdirSync(folderPath, { withFileTypes: true });
    let exportsLines = [];
    for (const item of items) {
        const itemPath = path.join(folderPath, item.name);
        if (item.isDirectory()) {
            exportsLines = exportsLines.concat(getExportsLines(itemPath));
        } else if (item.isFile() && item.name.endsWith('.ts')) {
            const filePath = path.relative(path.resolve(__dirname, '..'), itemPath).replace(/\\/g, '/');
            exportsLines.push(`export * from '${filePath.slice(0, -3)}';`);
        }
    }
    return exportsLines;
}

const exportsLines = getExportsLines(path.resolve(__dirname, '..', typesFolder));

fs.writeFileSync(
    path.resolve(__dirname, '..', 'index.d.ts'),
    `/*
 * This file is autogenerated and should not be changed manually!
*/\r\n
declare module '${packageName}' {
    ${exportsLines.join('\r\n\t')}
}
`,
);
